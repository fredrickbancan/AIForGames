#include "Game.h"

int main(int argc, char* argv[])
{
    // Initialization
    //--------------------------------------------------------------------------------------
    Game* gameInstance = Game::get();  
    //--------------------------------------------------------------------------------------

    // Main game loop
    while (!gameInstance->isClosing())    // Detect window close button or ESC key
    {
        // Update
        //----------------------------------------------------------------------------------
        gameInstance->onFrame();
        //----------------------------------------------------------------------------------

        // Draw
        //----------------------------------------------------------------------------------   
        gameInstance->drawScene();
        //----------------------------------------------------------------------------------
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------   
    Game::close();
    //--------------------------------------------------------------------------------------
    return 0;
}

